package zc.training.practice;

public class TypesOfMethods {
	// static method
	public static boolean isEven(int number) {
		if (number % 2 == 0) {
			return true;
		} else {
			return false;
		}
	}

	public boolean isOdd(int number) {
		if (number % 2 != 0) {
			return true;
		} else {
			return false;
		}

	}

}
